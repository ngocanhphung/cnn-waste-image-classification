"""## M2 - CNN (1 block)"""

# ── M2: Tiny CNN ──────────────────────────────────────────────────────────────
# Architecture rationale:
#   Introduces a single convolutional block (Conv → Pool) before the dense head.
#   The 3×3 kernel slides across the image preserving spatial relationships
#   between neighbouring pixels — the fundamental advantage of CNNs over ANNs
#   for image data. 32 filters detect 32 distinct low-level features (edges,
#   colour blobs). MaxPooling halves spatial dimensions, providing translation
#   invariance: a cardboard edge detected slightly left or right still activates
#   the same filter. Dropout(0.25) after pooling and Dropout(0.5) in the dense
#   head provide early regularisation. No BatchNorm — deliberately absent to
#   isolate the effect of adding it in M5.

def build_m2():
    model = Sequential([
        Conv2D(32, (3,3), activation='relu', padding='same',
               input_shape=(img_rows, img_cols, channels)),
        MaxPooling2D(2, 2),
        Flatten(),
        Dense(128, activation='relu'),
        Dense(num_classes, activation='softmax')
    ], name='M2_Tiny_CNN')
    model.summary()
    return model

