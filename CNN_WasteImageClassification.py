#   stabilising the distribution of inputs to each layer during training.
#   This reduces internal covariate shift, allows higher learning rates, and
#   acts as a mild regulariser by adding noise through batch statistics.
#   Placement: after Conv, before activation (standard convention).
#   Comparing M4 vs M5 directly answers: is dropout or BatchNorm more effective
#   as a regulariser on this dataset and architecture?

