"""## M3 - CNN (3 block, x2 filter)"""

# ── M3: Deeper CNN ────────────────────────────────────────────────────────────
# Architecture rationale:
#   Extends M2 to three convolutional blocks with a standard doubling filter
#   progression (32 → 64 → 128). Each block learns increasingly abstract features:
#   block 1 detects edges and colour gradients; block 2 learns textures (the
#   corrugation of cardboard, the smoothness of glass); block 3 captures
#   object-level patterns (bottle shapes, crumpled surfaces).
#   No dropout or BatchNorm — deliberately omitted to isolate the effect of
