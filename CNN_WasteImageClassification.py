"""## M6 - CNN (GAP replaces Flatten)"""

# ── M6: GAP CNN ───────────────────────────────────────────────────────────────
# Architecture rationale:
#   Takes the best regulariser from M4/M5 (BatchNorm) and replaces Flatten
#   with GlobalAveragePooling2D (GAP). After the final conv block, the feature
#   map has shape (H, W, 128). Flatten unrolls this into H×W×128 values,
#   creating a massive Dense connection sensitive to object position.
#   GAP instead averages each of the 128 feature maps to a single scalar,
#   producing a compact 128-d vector regardless of spatial size. Benefits:
#   (1) dramatically fewer parameters — the Dense connection shrinks from
#   H×W×128×256 to 128×128; (2) built-in spatial invariance — a glass bottle
#   in the top-left vs bottom-right of the frame produces the same GAP output;
#   (3) reduced overfitting on small datasets.

def build_m6():
    model = Sequential([
        # Block 1
        Conv2D(32,  (3,3), padding='same',
               input_shape=(img_rows, img_cols, channels)),
        BatchNormalization(),
        tf.keras.layers.Activation('relu'),
        MaxPooling2D(2, 2),

        # Block 2
        Conv2D(64,  (3,3), padding='same'),
        BatchNormalization(),
        tf.keras.layers.Activation('relu'),
        MaxPooling2D(2, 2),

        # Block 3
        Conv2D(128, (3,3), padding='same'),
        BatchNormalization(),
        tf.keras.layers.Activation('relu'),
        MaxPooling2D(2, 2),

        GlobalAveragePooling2D(),
        Dense(128, activation='relu'),
        Dense(num_classes, activation='softmax')
    ], name='M6_GAP_CNN')
    model.summary()
    return model

m6 = build_m6()
m6.compile(optimizer='adam',
           loss='sparse_categorical_crossentropy',
           metrics=['accuracy'])

history_m6 = m6.fit(
    datagen_light.flow(X_train, y_train, batch_size=BATCH_SIZE),
    epochs=30,
    validation_data=(X_test, y_test),
    callbacks=get_callbacks(),
    verbose=1
)

plot_history(history_m6, 'M6 — GAP CNN')
results_m6 = evaluate_model(m6, X_test, y_test, 'M6 — GAP CNN')

