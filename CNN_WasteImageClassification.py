"""## Shared Utilities"""

def get_callbacks(patience_es=10, patience_lr=5):
    return [
        EarlyStopping(monitor='val_loss', patience=patience_es,
                      restore_best_weights=True, verbose=1),
        ReduceLROnPlateau(monitor='val_loss', factor=0.5,
                          patience=patience_lr, min_lr=1e-6, verbose=1)
    ]

def plot_history(history, model_name):
    fig, axes = plt.subplots(1, 2, figsize=(13, 4))
    axes[0].plot(history.history['accuracy'],     label='Train')
    axes[0].plot(history.history['val_accuracy'], label='Validation')
    axes[0].set_title(f'{model_name} — Accuracy')
    axes[0].set_xlabel('Epoch')
    axes[0].legend()

    axes[1].plot(history.history['loss'],     label='Train')
    axes[1].plot(history.history['val_loss'], label='Validation')
    axes[1].set_title(f'{model_name} — Loss')
    axes[1].set_xlabel('Epoch')
    axes[1].legend()

    plt.tight_layout()
    plt.show()

def evaluate_model(model, X, y_true, model_name):
    loss, acc = model.evaluate(X, y_true, verbose=0)
    y_pred = np.argmax(model.predict(X, verbose=0), axis=1)

    print(f"\n{'='*60}")
    print(f"  {model_name}")
    print(f"  Test Accuracy : {acc:.4f}  |  Test Loss : {loss:.4f}")
    print('='*60)
    print(classification_report(y_true, y_pred, target_names=CLASSES))

    # Normalised confusion matrix
    cm = confusion_matrix(y_true, y_pred, normalize='true')
    plt.figure(figsize=(8, 6))
    sns.heatmap(cm, annot=True, fmt='.2f', cmap='Blues',
                xticklabels=CLASSES, yticklabels=CLASSES)
    plt.title(f'{model_name} — Normalised Confusion Matrix')
    plt.ylabel('True Label')
    plt.xlabel('Predicted Label')
    plt.tight_layout()
    plt.show()

    return {
        'model'   : model_name,
        'accuracy': acc,
        'loss'    : loss,
        'y_pred'  : y_pred,
        'report'  : classification_report(y_true, y_pred,
                                          target_names=CLASSES,
                                          output_dict=True)
    }

