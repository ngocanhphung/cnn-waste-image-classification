"""## M10 - CNN (MobileNetV2 Fine-tuned)"""

# ── M10: MobileNetV2 Fine-Tuning ─────────────────────────────────────────────
# The frozen backbone in M9 achieved 81% — a marginal improvement over M5's
# 80.3%. This suggests the pretrained features, while useful, are not perfectly
# calibrated for waste classification. Fine-tuning the top layers allows the
# network to adapt high-level feature detectors toward waste-specific patterns:
# the translucency of glass, metallic sheen of cans, corrugation of cardboard.
#
# Top 30 layers are unfrozen — these encode the most task-specific, abstract
# representations in the backbone. Bottom layers remain frozen as they detect
# universal low-level features (edges, gradients) needing no adaptation.
#
# Learning rate drops from 1e-3 to 1e-5 — essential to prevent large gradient
# updates from destroying the pretrained weights. Small nudges adapt the
# features; large updates would reset them to random noise.

print("Unfreezing top 30 layers of MobileNetV2 backbone...")

base_model_m9.trainable = True
for layer in base_model_m9.layers[:-30]:
    layer.trainable = False

trainable_count = sum(1 for l in base_model_m9.layers if l.trainable)
print(f"Trainable backbone layers : {trainable_count} / {len(base_model_m9.layers)}")

# Recompile at fine-tuning learning rate
m9.compile(optimizer=tf.keras.optimizers.Adam(learning_rate=1e-5),
           loss='sparse_categorical_crossentropy',
           metrics=['accuracy'])

history_m10 = m9.fit(
    datagen_tl.flow(X_train_tl, y_train_tl, batch_size=BATCH_SIZE),
    epochs=20,
    validation_data=(X_test_tl, y_test_tl),
    callbacks=get_callbacks(patience_es=8, patience_lr=4),
    verbose=1
)

# Combined plot — shows full training arc from M9 head training → M10 fine-tune
def plot_combined_history(h1, h2, model_name):
    acc   = h1.history['accuracy']     + h2.history['accuracy']
    val   = h1.history['val_accuracy'] + h2.history['val_accuracy']
    loss  = h1.history['loss']         + h2.history['loss']
    vloss = h1.history['val_loss']     + h2.history['val_loss']
    boundary = len(h1.history['accuracy'])

    fig, axes = plt.subplots(1, 2, figsize=(13, 4))
    for ax, train, val_data, title in zip(
        axes,
        [acc, loss], [val, vloss],
        [f'{model_name} — Accuracy', f'{model_name} — Loss']
    ):
        ax.plot(train,    label='Train')
        ax.plot(val_data, label='Validation')
        ax.axvline(boundary, color='gray', linestyle='--',
                   label='Fine-tune start')
        ax.set_title(title)
        ax.set_xlabel('Epoch')
        ax.legend()

    plt.tight_layout()
    plt.show()

plot_combined_history(history_m9, history_m10, 'M9 → M10 MobileNetV2')
